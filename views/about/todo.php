<b>DEAF.city</b>
<p class="style1">The Deaf community lives in a world that often overlooks its natural way of communicating: sign language. While many Deaf people rely on lip-reading, most hearing individuals
don’t understand sign languages, resulting in isolation and invisibility. For many, the rare appearance of a sign-language interpreter on television is the only acknowledgment of Deaf existence.</p>

<b>Breaking Silence</b>
<p class="style1">DEAF.city uses visual-gestural humor to challenge hearing indifference, to celebrate Deaf culture and connect communities. Participants share stories and jokes, turning everyday
misunderstandings into moments of laughter and reflection. Humor becomes a bridge between Deaf and hearing audiences, empowering storytellers to reclaim visibility and promote inclusion.</p>

<b>Dissemination</b>
<p class="style1">The project spreads across an open-access video repository with multi-screen installations in museums, art centers and public spaces, using TVs, LED panels, or projections.
The videos—paired with participant-generated sounds such as vocalizations, claps, and finger snaps—create a rich, engaging soundscape.</p>
<p class="style1"></p>

<b>Timeline</b>
<p class="style1">Since its launch in Valencia in 2020, DEAF.city has expanded to Mexico City in 2021, and in 2023 to Bilbao and São Paulo. Twenty-six participants have shared humorous monologues
in Spanish, Mexican, and Brazilian Sign Languages. In 2026, the project will grow to Marseille, Rome, Athens, Istanbul, Tunis, Algiers, and Barcelona, incorporating French, Italian, Greek,
Turkish, Tunisian, Algerian, and Catalan sign languages.</p>

<b>Silent Eloquence</b>
<p class="style1">By blending humor, art, and activism, DEAF.city makes Deaf culture visible, fosters understanding, and builds bridges between Deaf and hearing communities worldwide.</p>
